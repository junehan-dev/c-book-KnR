int	ft_stack_swap(void **s1, void **s2)
{
	void	*temp;

	temp = (*s1 && *s2) ? *s2 : (void *)0;
	*s2 = (temp) ? *s1 : *s2;
	*s1 = (temp) ? temp : *s1;

	return ((temp) ? 1 : 0);
}
